package leetcode.p2200_2299.p2231_largest_number_after_digit_swaps_by_parity;

public interface LargestNumberAfterDigitSwapsByParity {
    /**
     * You are given a positive integer num. You may swap any two digits
     * of num that have the same parity (i.e. both odd digits or both
     * even digits).
     * <p>
     * Return the largest possible value of num after any number of
     * swaps.
     *
     * @param num 1 <= num <= 109
     */
    int largestInteger(int num);
}
