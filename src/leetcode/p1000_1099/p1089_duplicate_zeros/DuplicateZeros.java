package leetcode.p1000_1099.p1089_duplicate_zeros;

public interface DuplicateZeros {
    /**
     * Given a fixed-length integer array arr, duplicate each occurrence
     * of zero, shifting the remaining elements to the right.
     * Note that elements beyond the length of the original array are
     * not written, Do the above modifications to the input array in
     * place and do not return anything.
     *
     * @param arr 1 <= arr.length <= 10^4
     *            0 <= arr[i] <= 9
     */
    void duplicateZeros(int[] arr);
}
