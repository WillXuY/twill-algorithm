package leetcode.p1200_1299.p1207_unique_number_of_occurrences;

public interface UniqueNumberOfOccurrences {
    /**
     * Given an array of integers arr, return true if the number of
     * occurrences of each value in the array is unique, or false
     * otherwise.
     *
     * @param arr 1 <= arr.length <= 1000
     *            -1000 <= arr[i] <= 1000
     * @return the number of occurrences is unique.
     */
    boolean uniqueOccurrences(int[] arr);
}
