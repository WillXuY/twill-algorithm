package org.willxu.algorithm.service.integer;

public interface FindNumbersWithEvenNumberOfDigits {
    /**
     * Given an array nums of integers, return how many of them contain
     * an even number of digits.
     *
     * @param nums 1 <= nums.length <= 500
     *             1 <= nums[i] <= 10^5
     * @return how many of nums contain an even number of digits.
     */
    int findNumbers(int[] nums);
}
