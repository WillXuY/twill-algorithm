package org.willxu.algorithm.service.integer;

public interface SubtractTheProductAndSumOfDigitsOfAnInteger {
    /**
     * Given an integer number n, return the difference between the
     * product of its digits and the sum of its digits.
     *
     * @param n 1 <= n <= 10^5
     * @return product subtract sum of digits of n.
     */
    int subtractProductAndSum(int n);
}
