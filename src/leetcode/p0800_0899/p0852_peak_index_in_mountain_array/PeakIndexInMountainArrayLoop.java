package leetcode.p0800_0899.p0852_peak_index_in_mountain_array;

public class PeakIndexInMountainArrayLoop implements PeakIndexInMountainArray {
    @Override
    public int peakIndexInMountainArray(int[] arr) {
        for (int i = 0; i < arr.length - 1; i++) {
            if (arr[i] > arr[i + 1]) {
                return i;
            }
        }
        return -1;
    }
}
