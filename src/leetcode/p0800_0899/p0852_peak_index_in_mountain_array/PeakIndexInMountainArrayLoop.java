package org.willxu.algorithm.service.impl.integer;

import org.willxu.algorithm.service.integer.PeakIndexInMountainArray;

public class PeakIndexInMountainArrayLoop implements PeakIndexInMountainArray {
    @Override
    public int peakIndexInMountainArray(int[] arr) {
        for (int i = 0; i < arr.length - 1; i++) {
            if (arr[i] > arr[i + 1]) {
                return i;
            }
        }
        return -1;
    }
}
