package leetcode.p2400_2499.p2427_number_of_common_factors;

public interface NumberOfCommonFactors {
    /**
     * Given two positive integers a and b, return the number of common
     * factors of a and b.
     * <p>
     * An integer x is a common factor of a and b if x divides both a
     * and b.
     *
     * @param a,b 1 <= a,b <= 1000
     */
    int commonFactors(int a, int b);
}
