package leetcode.p0600_0699.p0633_sum_of_square_numbers;

public interface SumOfSquareNumbers {
    /**
     * Given a non-negative integer c, decide whether there're two
     * integers a and b such that a^2 + b^2 = c.
     *
     * @param c 0 <= c <= 2^31 - 1
     */
    boolean judgeSquareSum(int c);
}
