package leetcode.p1400_1499.p1446_consecutive_characters;

public interface ConsecutiveCharacters {
    /**
     * The power of the string is the maximum length of a non-empty
     * substring that contains only one unique character.
     * Given a string s, return the power of s.
     *
     * @param s 1 <= s.length <= 500
     *          consists of only lowercase English letters.
     */
    int maxPower(String s);
}
