package leetcode.p1500_1599.p1523_count_odd_numbers_in_an_interval_range;

public interface CountOddNumbersInAnIntervalRange {
    /**
     * Given two non-negative integers low and high. Return the count of
     * odd numbers between low and high (inclusive).
     *
     * @param low 0 <= low <= high <= 10^9
     */
    int countOdds(int low, int high);
}
