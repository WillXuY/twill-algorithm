package leetcode.p1700_1799.p1748_sum_of_unique_elements;

public interface SumOfUniqueElements {
    /**
     * You are given an integer array nums. The unique elements of an
     * array are the elements that appear exactly once in the array.
     *
     * Return the sum of all the unique elements of nums.
     *
     * @param nums 1 <= nums.length <= 100
     *             1 <= nums[i] <= 100
     */
    int sumOfUnique(int[] nums);
}
