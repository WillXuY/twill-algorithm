package org.willxu.algorithm.service.integer;

public interface SecondLargestDigitInString {
    /**
     * Given an alphanumeric string s, return the second largest
     * numerical digit that appears in s, or -1 if it does not exist.
     *
     * An alphanumeric string is a string consisting of lowercase
     * English letters and digits.
     *
     * @param s 1 <= s.length <= 500
     *          consists of only lowercase English letters and/or digits
     */
    int secondHighest(String s);
}
