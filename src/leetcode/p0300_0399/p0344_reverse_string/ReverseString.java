package org.willxu.algorithm.service.empty;

public interface ReverseString {
    /**
     * Write a function that reverses a string. The input string is
     * given as an array of characters s.
     *
     * Follow up: Do not allocate extra space for another array. You
     * must do this by modifying the input array in-place with O(1)
     * extra memory.
     *
     * @param s 1 <= s.length <= 10^5
     *          s[i] is a printable ascii character.
     */
    void reverseString(char[] s);
}
