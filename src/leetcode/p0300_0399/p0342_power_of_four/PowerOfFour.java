package leetcode.p0300_0399.p0342_power_of_four;

public interface PowerOfFour {
    /**
     * Given an integer n, return true if it is a power of four.
     * Otherwise, return false. An integer n is a power of four, if
     * there exists an integer x such that n == 4^x.
     *
     * @param n -2^31 <= n <= 2^31 - 1
     * @return is power of 4 or not.
     */
    boolean isPowerOfFour(int n);
}
