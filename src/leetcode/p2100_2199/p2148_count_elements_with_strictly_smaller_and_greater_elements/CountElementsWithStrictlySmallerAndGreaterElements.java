package org.willxu.algorithm.service.integer;

public interface CountElementsWithStrictlySmallerAndGreaterElements {
    /**
     * Given an integer array nums, return the number of elements that
     * have both a strictly smaller and a strictly greater element
     * appear in nums.
     *
     * @param nums 1 <= nums.length <= 100
     *             -105 <= nums[i] <= 105
     */
    int countElements(int[] nums);
}
