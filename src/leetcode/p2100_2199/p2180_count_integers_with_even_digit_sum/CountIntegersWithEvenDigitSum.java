package leetcode.p2100_2199.p2180_count_integers_with_even_digit_sum;

public interface CountIntegersWithEvenDigitSum {
    /**
     * Given a positive integer num, return the number of positive
     * integers less than or equal to num whose digit sums are even.
     * <p>
     * The digit sum of a positive integer is the sum of all its digits.
     *
     * @param num 1 <= num <= 1000
     */
    int countEven(int num);
}
