package org.willxu.algorithm.service.ints;

public interface FindUniqueIntegersSumUpToZero {
    /**
     * Given an integer n, return any array containing n unique integers
     * such that they add up to 0.
     *
     * @param n 1 <= n <= 1000
     * @return any array containing n unique integers that add up to 0.
     */
    int[] sumZero(int n);
}
