package org.willxu.algorithm.service.integer;

public interface Maximum69Number {
    /**
     * You are given a positive integer num consisting only of digits 6
     * and 9.
     * Return the maximum number you can get by changing at most one
     * digit (6 becomes 9, and 9 becomes 6).
     *
     * @param num 1 <= num <= 10^4
     * @return change at most one digit in num.
     */
    int maximum69Number (int num);
}
